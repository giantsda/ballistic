double
calcFR (double Temperature, double Pressure, double RelativeHumidity)
{
  double VPw = 4e-6 * pow (Temperature, 3) - 0.0004 * pow (Temperature, 2)
      + 0.0234 * Temperature - 0.2517;
  double FRH = 0.995
      * (Pressure / (Pressure - (0.3783) * (RelativeHumidity) * VPw));
  return FRH;
}

double
calcFP (double Pressure)
{
  double Pstd = 29.53; // in-hg
  double FP = 0;
  FP = (Pressure - Pstd) / (Pstd);
  return FP;
}

double
calcFT (double Temperature, double Altitude)
{
  double Tstd = -0.0036 * Altitude + 59;
  double FT = (Temperature - Tstd) / (459.6 + Tstd);
  return FT;
}

double
calcFA (double Altitude)
{
  double fa = 0;
  fa = -4e-15 * pow (Altitude, 3) + 4e-10 * pow (Altitude, 2) - 3e-5 * Altitude
      + 1;
  return (1 / fa);
}

double
AtmCorrect (double DragCoefficient, double Altitude, double Barometer,
	    double Temperature, double RelativeHumidity)
{

  double FA = calcFA (Altitude);
  double FT = calcFT (Temperature, Altitude);
  double FR = calcFR (Temperature, Barometer, RelativeHumidity);
  double FP = calcFP (Barometer);

  // Calculate the atmospheric correction factor
  double CD = (FA * (1 + FT - FP) * FR);
  return DragCoefficient * CD;
}

double
getShootingAngle (double MV, double modifiedBC, double zeroDistance,
		  double windV, double windAngle, int DragFunction,
		  double scopeOffSet)
{
  double V0, dt, Wx, Wy, Wz, theta, da, Vx, Vy, Vz, x, y, z, t, k1, k2, k3, k4,
      V, Cd;
  int quit;
  double BC = modifiedBC;
// Initilize
  V0 = MV;
  dt = 1 / V0 / 2;
  windV = windV * 1.4666666666; //convert mpg to fps;
  Wx = windV * cos (DegtoRad (windAngle));
  Wz = windV * sin (DegtoRad (windAngle));
  Wy = 0;
  theta = DegtoRad (0);
  da = DegtoRad (14);
  quit = 0;

  while (!quit)
    {
      Vx = V0 * cos (theta);
      Vy = V0 * sin (theta);
      Vz = 0;

      x = 0;
      y = -scopeOffSet / 12;
      z = 0;
      t = 0;

      while (x / 3 < zeroDistance)
	{
	  V = sqrt (Vx * Vx + Vy * Vy + Vz * Vz);
	  Cd = getDrag (V, DragFunction) / BC;
	  // for Vx
	  k1 = dt * -(Cd * V * (Vx + Wx));
	  k2 = dt * -(Cd * V * (Vx + 0.5 * k1 + Wx));
	  k3 = dt * -(Cd * V * (Vx + 0.5 * k2 + Wx));
	  k4 = dt * -(Cd * V * (Vx + k3 + Wx));
	  Vx = Vx + 1. / 6 * k1 + 1. / 3 * k2 + 1. / 3 * k3 + 1. / 6 * k4;
	  // for Vy
	  k1 = dt * -(Cd * V * (Vy + Wy) + GRAVITY);
	  k2 = dt * -(Cd * V * (Vy + 0.5 * k1 + Wy) + GRAVITY);
	  k3 = dt * -(Cd * V * (Vy + 0.5 * k2 + Wy) + GRAVITY);
	  k4 = dt * -(Cd * V * (Vy + k3 + Wy) + GRAVITY);
	  Vy = Vy + 1. / 6 * k1 + 1. / 3 * k2 + 1. / 3 * k3 + 1. / 6 * k4;
	  // for Vz
	  k1 = dt * -Cd * V * (Vz - Wz);
	  k2 = dt * -Cd * V * (Vz + 0.5 * k1 - Wz);
	  k3 = dt * -Cd * V * (Vz + 0.5 * k2 - Wz);
	  k4 = dt * -Cd * V * (Vz + k3 - Wz);
	  Vz = Vz + 1. / 6 * k1 + 1. / 3 * k2 + 1. / 3 * k3 + 1. / 6 * k4;
	  x = x + Vx * dt;
	  y = y + Vy * dt;
	  z = z + Vz * dt;
	  t = t + dt;

	  if (Vy < 0 && y < 0)
	    break;

	  if (Vy > 3 * Vx)
	    break;
	}

      if (y > 0 && da > 0)
	da = -da / 2;

      if (y < 0 && da < 0)
	da = -da / 2;

      theta = theta + da;

      if (fabs (da) < MOAtoRad (0.0001))
	quit = 1;

      if (theta > DegtoRad (45))
	quit = 1;

    }

  return theta;
}

double
getDrag (double Velocity, int DragFunction)
{
  double *x, *y, *X;
  double xp = Velocity;
  switch (DragFunction)
    {
    case G1:
      x = G1Dragx;
      y = G1Dragy;
      X = G1DragX;
      break;
    case G2:
      x = G2Dragx;
      y = G2Dragy;
      X = G2DragX;
      break;
    case G5:
      x = G5Dragx;
      y = G5Dragy;
      X = G5DragX;
      break;
    case G6:
      x = G6Dragx;
      y = G6Dragy;
      X = G6DragX;
      break;
    case G7:
      x = G7Dragx;
      y = G7Dragy;
      X = G7DragX;
      break;
    case G8:
      x = G8Dragx;
      y = G8Dragy;
      X = G8DragX;
      break;
    }
  int k;
  double h, a, b;
  int klo = 0;
  int khi = sizeof(G1Dragx) / sizeof(G1Dragx[0]) - 1;
  while (khi - klo > 1)
    {
      k = (khi + klo) >> 1;
      if (x[k] > xp)
	khi = k;
      else
	klo = k;
    }
  h = x[khi] - x[klo];
  a = (x[khi] - xp) / h;
  b = (xp - x[klo]) / h;
  double Cd = a * y[klo] + b * y[khi]
      + ((a * a * a - a) * X[klo] + (b * b * b - b) * X[khi]) * (h * h) / 6.0;
  return Cd;
}

int
solveTrajectory (double MV, double shootingAngle, double modifiedBC,
		 double windV, double windAngle, int DragFunction,
		 double scopeOffSet, double distance, double **Results)
{
  double V0, dt, Wx, Wy, Wz, theta, Vx, Vy, Vz, x, y, z, t, k1, k2, k3, k4, V,
      Cd, xIntercept, yIntercept, elevation, windage;
  int i;
  double BC = modifiedBC;
// Initilize
  V0 = MV;
  dt = 1 / V0 / 2;
  windV = windV * 1.4666666666; //convert mpg to fps;
  Wx = windV * cos (DegtoRad (windAngle));
  Wz = windV * sin (DegtoRad (windAngle));
  Wy = 0;
  theta = shootingAngle;

  Vx = V0 * cos (theta);
  Vy = V0 * sin (theta);
  Vz = 0;

  i = 0;
  x = 0;
  y = -scopeOffSet / 12;
  z = 0;
  t = 0;

  while (x / 3 < distance)
    {
      V = sqrt (Vx * Vx + Vy * Vy + Vz * Vz);
      Cd = getDrag (V, DragFunction) / BC;
      // for Vx
      k1 = dt * -(Cd * V * (Vx + Wx));
      k2 = dt * -(Cd * V * (Vx + 0.5 * k1 + Wx));
      k3 = dt * -(Cd * V * (Vx + 0.5 * k2 + Wx));
      k4 = dt * -(Cd * V * (Vx + k3 + Wx));
      Vx = Vx + 1. / 6 * k1 + 1. / 3 * k2 + 1. / 3 * k3 + 1. / 6 * k4;
      // for Vy
      k1 = dt * -(Cd * V * (Vy + Wy) + GRAVITY);
      k2 = dt * -(Cd * V * (Vy + 0.5 * k1 + Wy) + GRAVITY);
      k3 = dt * -(Cd * V * (Vy + 0.5 * k2 + Wy) + GRAVITY);
      k4 = dt * -(Cd * V * (Vy + k3 + Wy) + GRAVITY);
      Vy = Vy + 1. / 6 * k1 + 1. / 3 * k2 + 1. / 3 * k3 + 1. / 6 * k4;
      // for Vz
      k1 = dt * -Cd * V * (Vz - Wz);
      k2 = dt * -Cd * V * (Vz + 0.5 * k1 - Wz);
      k3 = dt * -Cd * V * (Vz + 0.5 * k2 - Wz);
      k4 = dt * -Cd * V * (Vz + k3 - Wz);
      Vz = Vz + 1. / 6 * k1 + 1. / 3 * k2 + 1. / 3 * k3 + 1. / 6 * k4;
      x = x + Vx * dt;
      y = y + Vy * dt;
      z = z + Vz * dt;
      t = t + dt;

      xIntercept = x / 3;
      yIntercept = y * 12;
      elevation = -RadtoMil (atan (y / x));
      windage = RadtoMil (atan (z / x));

      Results[0][i] = xIntercept;
      Results[1][i] = elevation;
      Results[2][i] = windage;
      Results[3][i] = yIntercept;
      Results[4][i] = Vx;
      Results[5][i] = Vy;
      Results[6][i] = Vz;
      Results[7][i] = V;
      Results[8][i] = t;
      i++;
    }
  return i;
}

int
getDopeCard (double plotStartRange, double plotInterval, double distance,
	     double **Results, double **interpolatedResults, int Nx)
{
  double range;
  int i = 0, j, k;
  for (range = plotStartRange; range <= distance; range = range + plotInterval)
    {
      i++; // determine the size of dope card
    }
  int Nxp = i;
  double xp[Nxp];
  i = 0;
  for (range = plotStartRange; range <= distance; range = range + plotInterval)
    {
      xp[i] = range;
      i++;
    }

  for (i = 0; i < Nxp; i++)
    {
      interpolatedResults[i][0] = xp[i];
      for (j = 1; j < 9; j++)
	{
//compare xp[i] and Results[0][0] to Results[0][Nx-1];
	  int klo = 0;
	  int khi = Nx - 1;
	  while (khi - klo > 1)
	    {
	      k = (khi + klo) >> 1;
	      if (Results[0][k] > xp[i])
		khi = k;
	      else
		klo = k;
	    }
// interpolate for 8 fields.
	  interpolatedResults[i][j] = Results[j][klo]
	      + (xp[i] - Results[0][klo]) / (Results[0][khi] - Results[0][klo])
		  * (Results[j][khi] - Results[j][klo]);
	}
    }
  return Nxp;
}

